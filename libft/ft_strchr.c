#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	if (*s == '\0' && c == '\0')
		return ((char *)s);
	if (ft_strlen(s) == 0)
		return (NULL);
	while (*s)
	{
		if (*s == (char)c)
			return ((char *)s);
		s++;
	}
	if (*s == '\0' && c == '\0')
		return ((char *)s);
	return (NULL);
}
